# Operators example 

a = 10
b = 3

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)

# Comparison operators

x = 5
y = 10

print(x > y)
print(x < y)
print(x == y)

# If-Else examples

age = 20

if age >= 18:
    print("Adult")
else:
    print("Minor")

# If-Elif-Else

marks = 85

if marks >= 90:
    print("Grade A")
elif marks >= 75:
    print("Grade B")
else:
    print("Grade C")
